public class bitwise {
    public static void main(String[] args) {
        System.out.print("bitwise & operation:");
        System.out.println(5&3);// 101 &011
        System.out.print("bitwise | or:");
        System.out.println(5|3); // 101|011
        System.out.print("bitwise xor operation:");
        System.out.println(5^3); // same bit take zeroes and different bit gives 1
        System.out.print("Left shift bitwise operator:");
        System.out.println(5<<3);
        System.out.print("Right shift betwise operator:");
        System.out.println(5>>3);
    }
}
